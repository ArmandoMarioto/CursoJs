 /*
 Aula 1 - Operadores de comparação
 Operadores de comparação
 > Maior que
 >= maior que ou igual a
 < menor que
 <= menor que ou igual a
 == igualdade
 === igualdade estrita (valor e tipo)
 != diferente
 !== diferente estrita
 */

 const comp = 10 > 5;
 console.log(comp);
 console.log(10 >= 11);
 console.log(10 < 11);
 console.log(10 <= 11);
 console.log(10 == '10');
 console.log(10 === '10');
 console.log(10 != 11);
 console.log(10 !== 11);